let CTV_LANGUAGE = localStorage.getItem('ctv-language') || 'en';
if (!CTV_TRANSLATIONS[CTV_LANGUAGE]) CTV_LANGUAGE = 'en';

function t(key, vars = {}) {
  const source = CTV_TRANSLATIONS[CTV_LANGUAGE] || CTV_TRANSLATIONS.en;
  let value = source[key] ?? CTV_TRANSLATIONS.en[key] ?? key;
  return Object.entries(vars).reduce(
    (text, [name, replacement]) => text.replaceAll(`{${name}}`, String(replacement ?? '')),
    value,
  );
}

function applyTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(element => {
    element.textContent = t(element.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-html]').forEach(element => {
    element.innerHTML = t(element.dataset.i18nHtml);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });
  root.querySelectorAll('[data-i18n-title]').forEach(element => {
    element.title = t(element.dataset.i18nTitle);
  });
  root.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
    element.setAttribute('aria-label', t(element.dataset.i18nAriaLabel));
  });
  document.documentElement.lang = CTV_LANGUAGE;
  const selector = document.getElementById('language-select');
  if (selector) selector.value = CTV_LANGUAGE;
}

function setLanguage(language) {
  if (!CTV_TRANSLATIONS[language]) return;
  CTV_LANGUAGE = language;
  localStorage.setItem('ctv-language', language);
  applyTranslations();
  if (typeof window._ctvRefreshLanguage === 'function') window._ctvRefreshLanguage();
}

function ctvLocale() {
  return CTV_LANGUAGE === 'it' ? 'it-IT' : 'en-GB';
}

window.t = t;
window.setLanguage = setLanguage;
window.applyTranslations = applyTranslations;
window.ctvLocale = ctvLocale;
